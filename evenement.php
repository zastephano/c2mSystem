	<h5 style="color:darkgreen;font-weight: bold;"><?php echo$lang['1. La phase éliminatoire'] ?></h5>
	<p><?php echo$lang["La phase éliminatoire a déroulé dans les établissements entre le 25 mars et 03 avril.
	Afin d'accélérer le travail et de mieux assurer son déroulement, nous avons délégué a  chaque membre de l'équipe trois lycées pour réaliser le premier test, en plus de prendre en charge la communication et l'information de ces établissements.
	Chaque membre est accompagné dans le jour du premier test avec un collégue ou deux pour le soutenir en matiére de surveillance dans les salles de test ainsi que la documentation du projet.
	Le soutien moral, le coatching et la représentation de l'Olympiade étaient parmi les éléments présents fortement au début de chaque rencontre avec les éléves afin de mieux les motiver a  réussir.
	Le test qui a duré deux heures a couvert plusieurs axes de l'informatique et surtout du programme du tronc commun (architecture des ordinateurs, réseaux, algorithme, â€¦).
	Les éléves sélectionnés ont été invoqués a  se présenter le jour de final accompagnés par leur tuteurs. En plus de son encouragement, le personnel des établissements a participé efficacement dans cette étape."] ?></p>
	<h5 style="color:darkgreen;font-weight: bold;"><?php echo$lang['2. La phase finale'] ?></h5>
	<p><?php echo$lang["La phase finale de l'lympiade était organisée a  l'Ecole Normale Supérieure de l'Enseignement Technique de Mohammedia le samedi 18 avril 2015.
	Nous avons réparti entre nous les services (Préparation de l'amphi et de la salle du test, accueil, préparation de la pause-café, animation,â€¦) dans le but de bien gérer le travail.
	Aprés le mot de notre encadrante du projet Madame Souad AHRIZ, Zouhair EL KOUCHI a présenté une description générale sur la premiére phase d'lympiade.
	La conférence organisée a vécu l'intervention d'un comité de personnes professionnelles connues par leurs riches expériences. Le coatch en développement personnel Monsieur Anass SAYEGH a parlé des secrets de l'excellence scolaire. Ainsi, le professeur et l'inventeur Monsieur ALMOHADAB ALMUSTAFA a présenté un exposé sur l'intégration interactive de la technologie de l'information et la communication dans l'éducation et la formation. Ensuite, le coatch en développement personnel Monsieur Said RAZZAK a montré l'importance de la créativité dans le succés et la réalisation des objectifs.
	Trois éléves de chaque lycée participe a  l'Olympiade, l'ensemble des candidats motivés et pleins de compétitivité a montré un niveau trés satisfaisant et surtout en algorithme et programmation. L'éléve Mohamed OURAGHE de lycée Ben Yassine a remporté le premier prix, Tandis que les deux prix suivants ont été remprotés par Mohamed Aymane OUGGADI et Zakaria Badi éléves du lycée Abdelekrim El khattabi. Les prix offerts étaient une tablette et une formation en langue francaise de valeur de 1000 DH pour le premier gagnant assurée par le centre Almaarifa Fedala (cf. Annexe 8), tandis que les autres gagnants ont recu des équipements informatiques.
	Tous les qualifiés au final ont eu des attestations de participation en les félicitant de leur arrivée a  la finale. L'événement était couvert par Zenata News, ce dernier a réalisé une interview avec les intervenants et les organisateurs."] ?></p>
